import {create} from "zustand"
import {persist} from "zustand/middleware"
import type { LoginType, RegisterType, UserType } from "@/types/auth.type"
import { API } from "@/lib/axios-client";
import { toast } from "sonner";
import { useSocket } from "./use-socket";

interface AuthState {
    user: UserType | null; 
    isLoggingIn: boolean;
    isSigningUp: boolean;
    isAuthStatusLaoding:boolean;

    register: (data:RegisterType)=>void;
    login: (data: LoginType)=>void;
    logout: ()=>void;

    isAuthStatus:()=>void

}

export const useAuth = create<AuthState>()(
    persist(
        (set)=>({
            user:null,
            isSigningUp:false,
            isLoggingIn:false,
            isAuthStatusLaoding: false,

            register:async(data:RegisterType)=>{
                set({isSigningUp:true});
                try{
                    const response =await API.post("/auth/register" ,data);
                    set({user:response.data.user});
                    useSocket.getState().connectSocket();
                    toast.success("Registered Sucessfully")
                }
                catch(err:any){
                    toast.error(err.response?.data?.message|| "registaration failed")
                }
                finally{
                    set({isSigningUp:false});
                }
            },


            login:async(data: LoginType)=>{
                set({isLoggingIn:true});
                try{
                    const response =await API.post("/auth/login" ,data);
                    set({user:response.data.user});
                    useSocket.getState().connectSocket();
                    toast.success("Login Sucessfully")
                }
                catch(err:any){
                    toast.error(err.response?.data?.message|| "Login failed")
                }
                finally{
                    set({isLoggingIn:false});
                }
            },


            logout:async()=>{
                try{
                    await API.post("/auth/logout");
                    set({user:null});
                    useSocket.getState().dissconnectSocket();
                    toast.success("Loged out Sucessfully")
                }
                catch(err :any){
                    toast.error(err.response?.data?.message|| "Logout failed")
                }
            },


            isAuthStatus:async()=>{
                set({isAuthStatusLaoding:true})
                try{
                    const response =await API.get("/auth/status");
                    set({user:response.data.user});
                    useSocket.getState().connectSocket();
                }
                catch(err:any){
                    toast.error(err.response?.data?.message|| "Authentication failed")
                    console.log(err)


                }
                finally{
                    set({isAuthStatusLaoding:false});
                }
            }

        }),
        {
            name:"whop:root"
        }
    )
)